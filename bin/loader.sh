#!/usr/bin/env bash
# cc-agents-md: Loads AGENTS.md files for Claude Code SessionStart hook.
# Walks from $CLAUDE_PROJECT_DIR up to git root, outputs root-first.
# Writes combined content to .claude/CLAUDE.md for reliable loading
# (bypasses Claude Code's hook stdout size limit).
# All errors silenced — never block a session.

PROJECT="${CLAUDE_PROJECT_DIR:-.}"

# Resolve to absolute path
PROJECT="$(cd "$PROJECT" 2>/dev/null && pwd)" || exit 0

# Find git root or use project dir
ROOT="$(git -C "$PROJECT" rev-parse --show-toplevel 2>/dev/null)" || ROOT="$PROJECT"

# Walk from project dir up to root, collecting AGENTS.md paths
files=()
dir="$PROJECT"
while :; do
  [ -f "$dir/AGENTS.md" ] && files+=("$dir/AGENTS.md")
  [ "$dir" = "$ROOT" ] && break
  parent="$(dirname "$dir")"
  [ "$parent" = "$dir" ] && break
  dir="$parent"
done

# Nothing found — clean up stale generated file and exit
if [ ${#files[@]} -eq 0 ]; then
  target="$ROOT/.claude/CLAUDE.md"
  if [ -f "$target" ] && head -1 "$target" 2>/dev/null | grep -q 'cc-agents-md'; then
    rm -f "$target" 2>/dev/null
  fi
  exit 0
fi

# Reverse array: root first, deeper last (general → specific)
reversed=()
for (( i=${#files[@]}-1; i>=0; i-- )); do
  reversed+=("${files[$i]}")
done

# Write to .claude/CLAUDE.md (auto-loaded by Claude Code, no size limit)
target="$ROOT/.claude/CLAUDE.md"
if mkdir -p "$ROOT/.claude" 2>/dev/null; then
  {
    echo "<!-- Auto-generated by cc-agents-md. Do not edit. -->"
    for f in "${reversed[@]}"; do
      prefix="$ROOT/"
      rel="${f#$prefix}"
      [ "$rel" = "$f" ] && rel="$(basename "$f")"
      echo ""
      echo "# AGENTS.md — ${rel}"
      echo ""
      cat "$f" 2>/dev/null
    done
  } > "$target" 2>/dev/null
fi

# Small status output (stays under any stdout limit)
list=""
for f in "${reversed[@]}"; do
  prefix="$ROOT/"
  rel="${f#$prefix}"
  [ "$rel" = "$f" ] && rel="$(basename "$f")"
  [ -n "$list" ] && list="$list, "
  list="$list$rel"
done
echo "[cc-agents-md] Loaded ${#reversed[@]} AGENTS.md file(s) into .claude/CLAUDE.md: ${list}"

exit 0
